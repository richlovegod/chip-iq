def rows(raw):
    return [{
        "broker_name": n,
        "net_shares": v,
        "avg_price": p,
        "share_pct": pct,
        "is_market_maker": n in MARKET_MAKERS,
        "is_other": n == "其他分點",
    } for n, v, p, pct in raw]


def window(buy, sell):
    b, s = rows(buy), rows(sell)
    return {
        "buy": b, "sell": s,
        "total_buy": sum(r["net_shares"] for r in b),
        "total_sell": sum(r["net_shares"] for r in s),
    }


